      public void book_load()
    {
        btnupdate.setEnabled(false);
        btndelete.setEnabled(false);
        int c;
        try {
            pst=con.prepareStatement("select* from bookmaster");
            rs=pst.executeQuery();
            ResultSetMetaData rsd=rs.getMetaData();
            c=rsd.getColumnCount();
            d=(DefaultTableModel)tablebook.getModel();
            d.setRowCount(0);
            while(rs.next())
            {
                Vector v2=new Vector();
                for(int i=1;i<=c;i++)
                {
                    v2.add(rs.getString("bookid"));
                    v2.add(rs.getString("booktitle"));
                    v2.add(rs.getString("author"));
                    v2.add(rs.getString("publisher"));
                    v2.add(rs.getString("year"));
                    v2.add(rs.getString("edition"));
                    v2.add(rs.getString("bookcategory"));
                    v2.add(rs.getString("copies"));
                     v2.add(rs.getString("avacopies"));
                    v2.add(rs.getString("language"));
                   
                }
                d.addRow(v2);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(book.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
