    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
         try {
            // TODO add your handling code here:
            String name=txttitle.getText();
            String author=txtauthor.getText();
            String publisher=txtpublisher.getText();
            String year=txtyear.getText();
            String edition=txtedition.getText();
            String category=txtcategory.getSelectedItem().toString();
            int copies=Integer.parseInt(txtcopy.getText());
            String language=txtlanguage.getText();
            
            pst=con.prepareStatement("insert into bookmaster(booktitle,author,publisher,year,bookcategory,copies,language,avacopies,edition)values(?,?,?,?,?,?,?,?,?)");
            pst.setString(1,name);
            pst.setString(2,author);
            pst.setString(3,publisher);
            pst.setString(4,year);
            pst.setString(5,category);
            pst.setInt(6,copies);
            pst.setString(7,language);
            pst.setInt(8,copies);
            pst.setString(9,edition);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Book added");
            txttitle.setText("");
           
            txtauthor.setText("");
            txtpublisher.setText("");
            txtyear.setText("");
            txtedition.setText("");
            txtlanguage.setText("");
            txtcopy.setText("");
            txtcategory.setSelectedIndex(-1);
            book_load();
        
        } catch (SQLException ex) {
            Logger.getLogger(book.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnsaveActionPerformed

